package Chapter01;

public class Identifier {
    /*
        标识符：可以用来标识类名、接口名、方法名、变量名、常量名等的都叫标识符
        标识符由数字、英文字母、下划线、美元符$组成，但是不能以数字开头
        对于标识符其实就是起名的一个过程，尽量做到见名知意
        对于类名/接口名各个单词的首字母都要大写，对于变量名/方法名做到首个单词的首字母小写，其余单词首字母大写

        例如：myName，字，My_name，Points，$points，_sys_ta，OK，_23b，_3_，#name，25name，class，&time，if，Hello World 哪些可以用作标识符？
        #name, 25name, &time, Hello World 这四个都无法用作标识符，不符合规范
        class, if 也无法用作标识符，因为它们是关键字，关键字不能用作标识符，除了这两类其余的都可以用作标识符
    */
}
