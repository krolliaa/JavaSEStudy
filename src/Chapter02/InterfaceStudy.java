package Chapter02;

public class InterfaceStudy {
    /*
        接口是一个比抽象类还要抽象的类，抽象类允许有实现方法的存在但是接口不允许，绝对的不允许，接口里面的方法全部都是抽象方法
        接口最主要解决了一个问题：单继承的问题，一个类只能继承单个类虽然说可以间接继承但是终不能改变单继承的事实，但是接口不一样，一个类可以实现多个接口。
        接口默认的方法修饰符：public abstract
        接口默认的变量修饰符：public static final
        1.接口不能被实例化所以没有构造函数这个概念
        2.接口之间可以继承但是不能被另外一个接口所实现，实现的是应该是类
    */
}
